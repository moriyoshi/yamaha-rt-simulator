package simulator

import "context"

var CmdNgnType = &CommandSpec{
	[]Token{TNgn, TType}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoNgnType = &CommandSpec{
	[]Token{TNo, TNgn, TType}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

