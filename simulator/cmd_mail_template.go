package simulator

import "context"

var CmdMailTemplate = &CommandSpec{
	[]Token{TMail, TTemplate}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoMailTemplate = &CommandSpec{
	[]Token{TNo, TMail, TTemplate}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

