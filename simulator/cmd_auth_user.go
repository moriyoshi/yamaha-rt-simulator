package simulator

import "context"

var CmdAuthUser = &CommandSpec{
	[]Token{TAuth, TUser}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoAuthUser = &CommandSpec{
	[]Token{TNo, TAuth, TUser}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

