package simulator

import "context"

var CmdTunnelMultipointLimit = &CommandSpec{
	[]Token{TTunnel, TMultipoint, TLimit}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		if sess.Tunnel == 0 {
			return InvalidCommandName
		}
		return nil
	},
}

var CmdNoTunnelMultipointLimit = &CommandSpec{
	[]Token{TNo, TTunnel, TMultipoint, TLimit}, 4,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		if sess.Tunnel == 0 {
			return InvalidCommandName
		}
		return nil
	},
}
