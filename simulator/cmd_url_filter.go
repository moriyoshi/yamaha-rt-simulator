package simulator

import "context"

var CmdUrlFilter = &CommandSpec{
	[]Token{TUrl, TFilter}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoUrlFilter = &CommandSpec{
	[]Token{TNo, TUrl, TFilter}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}
