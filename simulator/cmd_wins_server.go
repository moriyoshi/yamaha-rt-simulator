package simulator

import "context"

var CmdWinsServer = &CommandSpec{
	[]Token{TWins, TServer}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

