package simulator

import "context"

var CmdMailNotify = &CommandSpec{
	[]Token{TMail, TNotify}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoMailNotify = &CommandSpec{
	[]Token{TNo, TMail, TNotify}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}
