package simulator

import "context"

var CmdTunnelNgnDisconnectTime = &CommandSpec{
	[]Token{TTunnel, TNgn, TDisconnect, TTime}, 4,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		if sess.Tunnel == 0 {
			return InvalidCommandName
		}
		return nil
	},
}

