package simulator

import "context"

var CmdUserAttribute = &CommandSpec{
	[]Token{TUser, TAttribute}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoUserAttribute = &CommandSpec{
	[]Token{TNo, TUser, TAttribute}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

