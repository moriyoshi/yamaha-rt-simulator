package simulator

import "context"

var CmdProviderSet = &CommandSpec{
	[]Token{TProvider, TSet}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoProviderSet = &CommandSpec{
	[]Token{TNo, TProvider, TSet}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

