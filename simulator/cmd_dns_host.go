package simulator

import "context"

var CmdDnsHost = &CommandSpec{
	[]Token{TDns, THost}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

