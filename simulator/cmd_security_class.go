package simulator

import "context"

var CmdSecurityClass = &CommandSpec{
	[]Token{TSecurity, TClass}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoSecurityClass = &CommandSpec{
	[]Token{TNo, TSecurity, TClass}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

