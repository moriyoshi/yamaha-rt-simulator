package simulator

import "context"

var CmdTunnelEndpointLocalAddress = &CommandSpec{
	[]Token{TTunnel, TEndpoint, TLocal, TAddress}, 4,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		if sess.Tunnel == 0 {
			return InvalidCommandName
		}
		return nil
	},
}

