package simulator

import "context"

var CmdIpRoute = &CommandSpec{
	[]Token{TIp, TRoute}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoIpRoute = &CommandSpec{
	[]Token{TNo, TIp, TRoute}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

