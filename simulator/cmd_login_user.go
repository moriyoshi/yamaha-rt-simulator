package simulator

import "context"

var CmdLoginUser = &CommandSpec{
	[]Token{TLogin, TUser}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoLoginUser = &CommandSpec{
	[]Token{TNo, TLogin, TUser}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}
