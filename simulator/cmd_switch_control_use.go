package simulator

import "context"

var CmdSwitchControlUse = &CommandSpec{
	[]Token{TSwitch, TControl, TUse}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoSwitchControlUse = &CommandSpec{
	[]Token{TNo, TSwitch, TControl, TUse}, 4,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

