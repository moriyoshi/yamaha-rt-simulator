package simulator

import "context"

var CmdPpBackupRecoveryTime = &CommandSpec{
	[]Token{TPp, TBackup, TRecovery, TTime}, 4,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		if sess.PP == 0 {
			return InvalidCommandName
		}
		return nil
	},
}

