package simulator

import "context"

var CmdUrlInterfaceFilter = &CommandSpec{
	[]Token{TUrl, TL2Interface, TFilter}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

