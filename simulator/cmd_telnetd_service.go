package simulator

import "context"

var CmdTelnetdService = &CommandSpec{
	[]Token{TTelnetd, TService}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

