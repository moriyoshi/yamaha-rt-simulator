package simulator

import "context"

var CmdTunnelNgnInterface = &CommandSpec{
	[]Token{TTunnel, TNgn, TInterface}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		if sess.Tunnel == 0 {
			return InvalidCommandName
		}
		return nil
	},
}

