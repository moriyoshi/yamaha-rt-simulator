package simulator

import "context"

var CmdLoginTimer = &CommandSpec{
	[]Token{TLogin, TTimer}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoLoginTimer = &CommandSpec{
	[]Token{TNo, TLogin, TTimer}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

