package simulator

import "context"

var CmdDnsServerPp = &CommandSpec{
	[]Token{TDns, TServer, TPp}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

