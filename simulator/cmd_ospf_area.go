package simulator

import "context"

var CmdOspfArea = &CommandSpec{
	[]Token{TOspf, TArea}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

