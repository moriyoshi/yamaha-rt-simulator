package simulator

import "context"

var CmdProviderSelect = &CommandSpec{
	[]Token{TProvider, TSelect}, 2,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

var CmdNoProviderSelect = &CommandSpec{
	[]Token{TNo, TProvider, TSelect}, 3,
	func(_ context.Context, sess *SimulatorSession, tis []TokenInstance) error {
		if !sess.Enabled {
			return AdministratorUseOnly
		}
		return nil
	},
}

